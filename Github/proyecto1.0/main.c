// --- VARIABLES GLOBALES DE TRABAJO ---
char mapa[MAPA_ALTO][MAPA_ANCHO];
char ventana[VENTANA_DIM][VENTANA_DIM];
Jugador jugador = {5, 2, 0, 0}; 
int nivel_actual = 1;

void cargar_nivel(int nivel) {
    for (int y = 0; y < MAPA_ALTO; y++) {
        for (int x = 0; x < MAPA_ANCHO; x++) {
            if (nivel == 1) {
                mapa[y][x] = mapa_nivel1[y][x];
            } else if (nivel == 2) {
                mapa[y][x] = mapa_nivel2[y][x];
            } else if (nivel == 3) {
                mapa[y][x] = mapa_nivel3[y][x];
            }
        }
    }
}

void renderizar() {
    system("cls"); 
    printf("===========================================\n");
    printf("   BITQUEST - NIVEL %d / 3                 \n", nivel_actual);
    printf("===========================================\n");
    printf("Monedas: %d | Llave: %s\n\n", jugador.monedas, jugador.tiene_llave ? "SI" : "NO");

    int cam_x = jugador.x - 10;
    int cam_y = jugador.y - 10;
    if (cam_x < 0) cam_x = 0; if (cam_x > 40) cam_x = 40;
    if (cam_y < 0) cam_y = 0; if (cam_y > 40) cam_y = 40;

    for (int y = 0; y < VENTANA_DIM; y++) {
        for (int x = 0; x < VENTANA_DIM; x++) {
            if ((cam_y + y) == jugador.y && (cam_x + x) == jugador.x) {
                printf("P "); 
            } else {
                printf("%c ", ventana[y][x]);
            }
        }
        printf("\n");
    }
    printf("\n[W/A/S/D] Moverse | [Q] Salir\n");
}

void procesar_movimiento(int dx, int dy) {
    int nuevo_x = jugador.x + dx;
    int nuevo_y = jugador.y + dy;

    if (nuevo_x >= 0 && nuevo_x < MAPA_ANCHO && nuevo_y >= 0 && nuevo_y < MAPA_ALTO) {
        char destino = mapa[nuevo_y][nuevo_x];

        if (destino != '#') { 
            if (destino == 'M') { 
                jugador.monedas++;
                mapa[nuevo_y][nuevo_x] = '.';
            } 
            else if (destino == 'K') { 
                jugador.tiene_llave = 1;
                mapa[nuevo_y][nuevo_x] = '.';
                printf("\a"); 
            } 
            else if (destino == 'D') { 
                if (jugador.tiene_llave) {
                    mapa[nuevo_y][nuevo_x] = '.'; 
                    jugador.tiene_llave = 0;      
                } else {
                    return; 
                }
            } 
            else if (destino == 'E') { 
                if (nivel_actual < 3) {
                    system("cls");
                    printf("\n\n\n\t====================================\n");
                    printf("\t ¡NIVEL %d COMPLETADO!\n", nivel_actual);
                    printf("\t Cargando el Siguiente Mapa...\n");
                    printf("\t====================================\n");
                    printf("\t Presiona cualquier tecla para continuar...");
                    _getch();

                    nivel_actual++;
                    cargar_nivel(nivel_actual);
                    
                    jugador.x = 5;
                    jugador.y = 2;
                    jugador.tiene_llave = 0;
                    return;
                } else {
                    system("cls");
                    printf("\n\n\n==================================================\n");
                    printf(" ¡VICTORIA ABSOLUTA EN BITQUEST!\n");
                    printf(" Has superado los 3 niveles con un total de %d monedas.\n", jugador.monedas);
                    printf("==================================================\n");
                    exit(0);
                }
            }
            
            jugador.x = nuevo_x;
            jugador.y = nuevo_y;
        }
    }
}

int main() {
    cargar_nivel(nivel_actual); 
    char tecla = 0;

    while (tecla != 'q' && tecla != 'Q') {
        recortar_ventana_asm((const char*)mapa, (char*)ventana, jugador.x, jugador.y);
        renderizar();

        tecla = _getch();
        switch (tecla) {
            case 'w': case 'W': procesar_movimiento(0, -1); break;
            case 's': case 'S': procesar_movimiento(0, 1);  break;
            case 'a': case 'A': procesar_movimiento(-1, 0); break;
            case 'd': case 'D': procesar_movimiento(1, 0);  break;
        }
    }
    return 0;
}